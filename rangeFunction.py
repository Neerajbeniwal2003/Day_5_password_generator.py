#Range func
#it is the function use with another function and it cant be use alone
#for example

#if we want to print numbers from 1 to 10

for i in range(1,11): #it will print 1 to 10 and does not include 11
    print(i)


#if we wanna print number from 1 to 10 with gaps

for i in range(1,11,2):
    print(i)

#program for sum of 1 to 100 numbers
sum=0
for i in range(1,101):
    sum+=i
print(sum)

